public class ClasseMATH {

	public static void main(String[] args) {
		
		System.out.println(Math.abs(-55)); //assoluto, toglie il "-"
		System.out.println(Math.min(55,21)); //minore
		System.out.println(Math.max(55,21));//maggiore
		System.out.println(Math.pow(5,3)); //potenza, 5 alla 3
		System.out.println(Math.sqrt(25)); //radice
		System.out.println(Math.PI+5); //Pi Greco
	}

}
