
public class Switch {

	public static void main(String[] args) {
		
		String nome = "Omar";
		
		switch(nome) {
		
		case "Omar":
			System.out.println("il nome è Omar!");
			break;
		case "Alessandro":
			System.out.println("il nome è Alessandro!");
			break;
		case "Andrea":
			System.out.println("il nome è Andrea!");
			break;
		default:
			System.out.println("non conosco il nome");
		}
	}

}
